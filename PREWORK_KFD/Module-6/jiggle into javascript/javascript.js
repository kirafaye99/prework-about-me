function grow(id) {
    id.style.width = "200px";
    id.style.height = "200px";
}
function blue(id) {
    id.style.backgroundColor = "blue";
}
function fade(id) {
    id.style.opacity = "0.5";
}
function reset(id) {
    id.style = "height:150px; width:150px; background-color:orange; margin:25px; opacity:1.0";
}